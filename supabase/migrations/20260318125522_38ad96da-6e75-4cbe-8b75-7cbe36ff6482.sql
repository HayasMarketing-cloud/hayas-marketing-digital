### Los 6 módulos de SENSE
1. **BRAIN** (Identidad Estratégica): Tu memoria de marketing centralizada. Propuesta de valor, ICPs, Buyer Personas, ventajas competitivas y voz de marca.
2. **RADAR** (Inteligencia de Mercado): Monitorización continua de industria y competidores. Señales priorizadas, informes de competidor con IA, y Market Reports.
3. **SEARCH** (Visibilidad e Intención): Seguimiento de keywords en Google, Bing y motores de IA (ChatGPT, Gemini, Perplexity). Auditorías de percepción de marca en LLMs.
4. **STRATEGY** (Dirección Estratégica): Drivers de mercado, directivas estratégicas y scoring de coherencia entre ejecución y estrategia.
5. **PLANS** (Ejecución y Activación): Planes de marketing generados desde inteligencia real. Content Studio, activaciones tipadas y propuestas con trazabilidad completa.
6. **WORKSPACE** (Centro de Operaciones): Gestión de fuentes, documentación estratégica, configuración de equipo e integraciones.

### IA contextualizada
La IA de SENSE no usa prompts genéricos. Cada output (reportes, contenido, planes) está anclado a la identidad de marca, audiencias y contexto competitivo de cada empresa. No es una IA que responde: es una IA que sabe quién eres.

### El framework LOOP
SENSE implementa un ciclo continuo: Listen (monitoriza señales) → Observe (detecta patrones) → Orient (define directivas) → Plan (genera activaciones alineadas).

